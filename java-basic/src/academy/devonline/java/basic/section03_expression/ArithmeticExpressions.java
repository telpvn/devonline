package academy.devonline.java.basic.section03_expression;

public class ArithmeticExpressions {
    public static void main(String[] args) {
        {
            var a = 5;
            var b = 3;

            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a * b);
            System.out.println(a / b);
            System.out.println(a % b);
        }
        System.out.println();
        {
            var a = 5.;
            var b = 3.;

            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a * b);
            System.out.println(a / b);
            System.out.println(a % b);
        }
    }
}
